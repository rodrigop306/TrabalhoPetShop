package model;

public class Pet {
	
	private int codPet;
	private String nomePet;
	private boolean vacinacaoPet;
	private String corPeloPet;
	private String descricaoPet;
	private String portePet;
	private String diaMortePet;
	private String horaMortePet;
	private Tutor codTutor;
	private Raca codRaca;
	
	
	public int getCodPet() {
		return codPet;
	}
	public void setCodPet(int codPet) {
		this.codPet = codPet;
	}
	public String getNomePet() {
		return nomePet;
	}
	public void setNomePet(String nomePet) {
		this.nomePet = nomePet;
	}
	public boolean isVacinacaoPet() {
		return vacinacaoPet;
	}
	public void setVacinacaoPet(boolean vacinacaoPet) {
		this.vacinacaoPet = vacinacaoPet;
	}
	public String getCorPeloPet() {
		return corPeloPet;
	}
	public void setCorPeloPet(String corPeloPet) {
		this.corPeloPet = corPeloPet;
	}
	public String getDescricaoPet() {
		return descricaoPet;
	}
	public void setDescricaoPet(String descricaoPet) {
		this.descricaoPet = descricaoPet;
	}
	public String getPortePet() {
		return portePet;
	}
	public void setPortePet(String portePet) {
		this.portePet = portePet;
	}
	public String getDiaMortePet() {
		return diaMortePet;
	}
	public void setDiaMortePet(String diaMortePet) {
		this.diaMortePet = diaMortePet;
	}
	public String getHoraMortePet() {
		return horaMortePet;
	}
	public void setHoraMortePet(String horaMortePet) {
		this.horaMortePet = horaMortePet;
	}
	public Tutor getCodTutor() {
		return codTutor;
	}
	public void setCodTutor(Tutor codTutor) {
		this.codTutor = codTutor;
	}
	public Raca getCodRaca() {
		return codRaca;
	}
	public void setCodRaca(Raca codRaca) {
		this.codRaca = codRaca;
	}
	
	
	
}
