package model;

public class Raca {
	
	private int codRaca;
	private String descricaoRaca;
	private int codEspecie;
	private int codAdmin;
	
	
	public int getCodRaca() {
		return codRaca;
	}
	public void setCodRaca(int codRaca) {
		this.codRaca = codRaca;
	}
	public String getDescricaoRaca() {
		return descricaoRaca;
	}
	public void setDescricaoRaca(String descricaoRaca) {
		this.descricaoRaca = descricaoRaca;
	}
	public int getCodEspecie() {
		return codEspecie;
	}
	public void setCodEspecie(int codEspecie) {
		this.codEspecie = codEspecie;
	}
	public int getCodAdmin() {
		return codAdmin;
	}
	public void setCodAdmin(int codAdmin) {
		this.codAdmin = codAdmin;
	}
	
	

}
